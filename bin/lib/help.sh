#!/bin/bash
#
# MechCrate Help & Version Commands
#

# Show help
show_help() {
    echo -e "${BOLD}mx${NC} - MechCrate Project Scaffolding Tool v${VERSION}"
    echo ""
    echo -e "${BOLD}USAGE:${NC}"
    echo "    mx <command> [options]"
    echo ""
    echo -e "${BOLD}COMMANDS:${NC}"
    echo "    new <name>        Create a new MechCrate project"
    echo "    upgrade           Update current project with latest scaffolding"
    echo "    add <service>     Add a new service to existing project"
    echo "    recipes           List and manage available recipes"
    echo "    infra             Manage global infrastructure credentials"
    echo "    router            Manage the global Traefik router"
    echo "    mcp               Manage the MCP server for LLM integration"
    echo "    cf <subcommand>   Cloudflare management commands"
    echo "    icr [dir]         Initialize coding rules and dev docs"
    echo "    test [recipe...]  Run recipe validation tests"
    echo "    doctor            Check project health and dependencies"
    echo "    version           Show version information"
    echo "    help              Show this help message"
    echo ""
    echo -e "${BOLD}RECIPES:${NC}"
    echo "    Recipes are pre-packaged stack components (Laravel, Nuxt, Rust, etc.)"
    echo "    that include all configuration, Docker setup, and Traefik routing."
    echo ""
    echo "    mx recipes                     List all available recipes"
    echo "    mx recipes info <recipe>       Show recipe details"
    echo "    mx add <name> --recipe=<type>  Add service using a recipe"
    echo ""
    echo -e "${BOLD}    Available Recipes:${NC}"
    echo "        laravel     PHP Laravel with Nginx, PHP-FPM, queues"
    echo "        nuxt        Nuxt 3 SSR/SSG with Tailwind + DaisyUI"
    echo "        rust-api    Rust Axum API with hexagonal architecture"
    echo ""
    echo -e "${BOLD}INFRASTRUCTURE COMMANDS (mx infra):${NC}"
    echo "    mx infra setup [provider]      Configure global credentials for a provider"
    echo "    mx infra list                  List all configured providers"
    echo "    mx infra inspect <provider>    Show config details for a provider"
    echo "    mx infra link <provider>       Link project to global config (from project)"
    echo "    mx infra unlink <provider>     Remove project link to global config"
    echo ""
    echo -e "${BOLD}    Supported Providers:${NC}"
    echo "        cloudflare    Cloudflare Workers & Containers"
    echo "        digitalocean  DigitalOcean Droplets & App Platform"
    echo "        aws           Amazon Web Services"
    echo "        hetzner       Hetzner Cloud"
    echo ""
    echo -e "${BOLD}ROUTER COMMANDS (mx router):${NC}"
    echo "    mx router install              Install the global Traefik router"
    echo "    mx router up                   Start or update the router"
    echo "    mx router down                 Stop the router"
    echo "    mx router status               Show router container status"
    echo "    mx router logs                 Tail router logs"
    echo "    mx router inspect              Show dashboard URL and connected services"
    echo "    mx router reload               Hot-reload config without restart"
    echo ""
    echo -e "${BOLD}CLOUDFLARE COMMANDS (mx cf):${NC}"
    echo "    mx cf setup                    Run Cloudflare setup wizard"
    echo "    mx cf init <app>               Initialize a new worker (interactive)"
    echo "    mx cf init <app> --type=TYPE   Initialize with specific type"
    echo "    mx cf status                   Show all Cloudflare apps"
    echo "    mx cf deploy <app>             Deploy to production"
    echo ""
    echo -e "${BOLD}    Worker Types:${NC}"
    echo "        worker      Standard edge worker (APIs, proxies)"
    echo "        cron        Scheduled worker (background jobs)"
    echo "        container   Docker container backend (SSR apps)"
    echo ""
    echo -e "${BOLD}PROJECT COMMANDS (run from project root):${NC}"
    echo "    dev [service]     Start services in development mode"
    echo "    up [service]      Start services in production mode"
    echo "    down [service]    Stop services"
    echo "    logs [service]    Tail service logs"
    echo "    build <service>   Build a service image (see Build Options below)"
    echo "    restart <service> Restart a service"
    echo "    sh <service>      Shell into a service"
    echo "    ps                List running services"
    echo ""
    echo -e "${BOLD}BUILD OPTIONS (mx build):${NC}"
    echo "    mx build <service>              Build dev image (default)"
    echo "    mx build <service> --prod       Build production-optimized image"
    echo "    mx build <service> -t v1.0.0    Build with custom tag"
    echo "    mx build <service> --push       Push image after build"
    echo "    mx build --help                 Show detailed build help"
    echo ""
    echo -e "${BOLD}OPTIONS:${NC}"
    echo "    --with <svc>      Include services immediately (runs 'mx add' internally)"
    echo "    --infra <type>    Include infrastructure (cloudflare, aws, digitalocean)"
    echo "    --recipe <type>   Use a recipe when adding a service"
    echo "    --no-prompt       Skip interactive prompts, use defaults"
    echo ""
    echo -e "${BOLD}EXAMPLES:${NC}"
    echo "    mx new my-app                          # Interactive setup"
    echo "    mx new my-app --no-prompt              # Quick setup, no prompts"
    echo "    mx new my-app --infra cloudflare       # With Cloudflare infra"
    echo "    mx upgrade                             # Update project with latest tools"
    echo "    mx upgrade --diff                      # Show diffs before updating"
    echo "    mx upgrade --yes                       # Auto-accept all updates"
    echo ""
    echo -e "${BOLD}    Recipe Examples:${NC}"
    echo "    mx add api --recipe=laravel            # Add Laravel stack"
    echo "    mx add web --recipe=nuxt               # Add Nuxt SSR app"
    echo "    mx add svc --recipe=rust-api           # Add Rust API service"
    echo "    mx add api --recipe=laravel --domain=api.myapp.com"
    echo ""
    echo -e "${BOLD}    Infrastructure Examples:${NC}"
    echo "    mx infra setup                         # Interactive provider selection"
    echo "    mx infra setup cloudflare              # Configure Cloudflare globally"
    echo "    mx infra setup digitalocean            # Configure DigitalOcean globally"
    echo "    mx infra list                          # Show all global configs"
    echo "    mx infra link cloudflare               # Link project to global config"
    echo ""
    echo -e "${BOLD}    Cloudflare Examples:${NC}"
    echo "    mx cf config                           # Show current config source"
    echo "    mx cf init api.example.com             # Initialize CF worker (interactive)"
    echo "    mx cf init my-job --type=cron          # Initialize cron worker"
    echo ""
    echo -e "${BOLD}    Build Examples:${NC}"
    echo "    mx build api                           # Dev build (for local development)"
    echo "    mx build api --prod                    # Production build (optimized, minimal)"
    echo "    mx build api --prod -t v1.2.0          # Production with version tag"
    echo "    mx build api --prod --push             # Build and push to registry"
    echo ""
    echo -e "${BOLD}    Code Rules Examples:${NC}"
    echo "    mx icr                                 # Initialize rules in current dir"
    echo "    mx icr ./my-project                    # Initialize in specific project"
    echo "    mx icr --skip-docs                     # Skip copying dev documentation"
    echo "    mx init-code-rules --help              # Show detailed icr help"
    echo ""
    echo -e "${BOLD}    Test Examples:${NC}"
    echo "    mx test                                # Run smoke tests on all recipes"
    echo "    mx test laravel                        # Test Laravel recipe only"
    echo "    mx test --level full laravel           # Run full integration tests"
    echo "    mx test --level build --keep           # Build tests, keep for debugging"
    echo "    mx test --help                         # Show detailed test help"
    echo ""
    echo -e "${BOLD}    Router Examples:${NC}"
    echo "    mx router install                      # First-time setup"
    echo "    mx router up                           # Start the global router"
    echo "    mx router inspect                      # See dashboard URL"
    echo "    mx router logs                         # Debug routing issues"
    echo ""
    echo -e "${BOLD}MCP SERVER (mx mcp):${NC}"
    echo "    mx mcp build                           # Build the MCP server"
    echo "    mx mcp start                           # Start Weaviate RAG backend"
    echo "    mx mcp ingest                          # Load documentation"
    echo "    mx mcp config                          # Show MCP client configuration"
    echo ""
    echo -e "${BOLD}QUICK START:${NC}"
    echo "    1. mx infra setup cloudflare           # Configure global credentials (once)"
    echo "    2. mx router install && mx router up   # Start global router"
    echo "    3. mx new myproject --infra cloudflare # Create project with CF"
    echo "    4. mx infra link cloudflare            # Link to global config"
    echo "    5. cd myproject && mx add api --recipe=laravel"
    echo "    6. mx dev                             # Start all services"
    echo "    7. mx dev s=api                        # Or start a specific service"
    echo ""
    echo -e "${CYAN}🦝 Crate Raccoon says: Happy building!${NC}"
}

# Show version
show_version() {
    echo "mx (MechCrate) version $VERSION"
}
